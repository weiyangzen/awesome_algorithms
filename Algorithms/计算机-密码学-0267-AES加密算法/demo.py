RCON = [
    0x00,
    0x01,
    0x02,
    0x04,
    0x08,
    0x10,
    0x20,
    0x40,
    0x80,
    0x1B,
    0x36,
]


def _gf_mul(a: int, b: int) -> int:
    result = 0
    x = a
    y = b
    for _ in range(8):
        if y & 1:
            result ^= x
        high_bit_set = x & 0x80
        x = (x << 1) & 0xFF
        if high_bit_set:
            x ^= 0x1B
        y >>= 1
    return result


def _sub_word(word: list[int]) -> list[int]:
    return [S_BOX[b] for b in word]


def _rot_word(word: list[int]) -> list[int]:
    return word[1:] + word[:1]


def _bytes_to_words(data: bytes) -> list[list[int]]:
    return [list(data[i : i + 4]) for i in range(0, len(data), 4)]


def expand_key(key: bytes) -> list[list[int]]:
    if len(key) != BLOCK_SIZE:
        raise ValueError("AES-128 key must be 16 bytes")

    words: list[list[int]] = _bytes_to_words(key)
    total_words = NB * (NR + 1)

    for i in range(NK, total_words):
        temp = words[i - 1].copy()
        if i % NK == 0:
            temp = _sub_word(_rot_word(temp))
            temp[0] ^= RCON[i // NK]
        words.append([words[i - NK][j] ^ temp[j] for j in range(4)])

    round_keys: list[list[int]] = []
    for r in range(NR + 1):
        rk: list[int] = []
        for w in words[r * NB : (r + 1) * NB]:
            rk.extend(w)
        round_keys.append(rk)
    return round_keys


def _add_round_key(state: list[int], round_key: list[int]) -> list[int]:
    return [s ^ k for s, k in zip(state, round_key)]


def _sub_bytes(state: list[int]) -> list[int]:
    return [S_BOX[b] for b in state]


def _inv_sub_bytes(state: list[int]) -> list[int]:
    return [INV_S_BOX[b] for b in state]


def _shift_rows(state: list[int]) -> list[int]:
    shifted = state.copy()
    for row in range(4):
        row_values = [state[row + 4 * col] for col in range(4)]
        row_values = row_values[row:] + row_values[:row]
        for col, value in enumerate(row_values):
            shifted[row + 4 * col] = value
    return shifted


def _inv_shift_rows(state: list[int]) -> list[int]:
    shifted = state.copy()
    for row in range(4):
        row_values = [state[row + 4 * col] for col in range(4)]
        row_values = row_values[-row:] + row_values[:-row] if row else row_values
        for col, value in enumerate(row_values):
            shifted[row + 4 * col] = value
    return shifted


def _mix_columns(state: list[int]) -> list[int]:
    mixed = state.copy()
    for col in range(4):
        s0, s1, s2, s3 = state[4 * col : 4 * col + 4]
        mixed[4 * col + 0] = _gf_mul(0x02, s0) ^ _gf_mul(0x03, s1) ^ s2 ^ s3
        mixed[4 * col + 1] = s0 ^ _gf_mul(0x02, s1) ^ _gf_mul(0x03, s2) ^ s3
        mixed[4 * col + 2] = s0 ^ s1 ^ _gf_mul(0x02, s2) ^ _gf_mul(0x03, s3)
        mixed[4 * col + 3] = _gf_mul(0x03, s0) ^ s1 ^ s2 ^ _gf_mul(0x02, s3)
    return mixed


def _inv_mix_columns(state: list[int]) -> list[int]:
    mixed = state.copy()
    for col in range(4):
        s0, s1, s2, s3 = state[4 * col : 4 * col + 4]
        mixed[4 * col + 0] = (
            _gf_mul(0x0E, s0) ^ _gf_mul(0x0B, s1) ^ _gf_mul(0x0D, s2) ^ _gf_mul(0x09, s3)
        )
        mixed[4 * col + 1] = (
            _gf_mul(0x09, s0) ^ _gf_mul(0x0E, s1) ^ _gf_mul(0x0B, s2) ^ _gf_mul(0x0D, s3)
        )
        mixed[4 * col + 2] = (
            _gf_mul(0x0D, s0) ^ _gf_mul(0x09, s1) ^ _gf_mul(0x0E, s2) ^ _gf_mul(0x0B, s3)
        )
        mixed[4 * col + 3] = (
            _gf_mul(0x0B, s0) ^ _gf_mul(0x0D, s1) ^ _gf_mul(0x09, s2) ^ _gf_mul(0x0E, s3)
        )
    return mixed


def encrypt_block(block: bytes, round_keys: list[list[int]]) -> bytes:
    if len(block) != BLOCK_SIZE:
        raise ValueError("AES block must be 16 bytes")

    state = list(block)
    state = _add_round_key(state, round_keys[0])

    for round_index in range(1, NR):
        state = _sub_bytes(state)
        state = _shift_rows(state)
        state = _mix_columns(state)
        state = _add_round_key(state, round_keys[round_index])

    state = _sub_bytes(state)
    state = _shift_rows(state)
    state = _add_round_key(state, round_keys[NR])
    return bytes(state)


def decrypt_block(block: bytes, round_keys: list[list[int]]) -> bytes:
    if len(block) != BLOCK_SIZE:
        raise ValueError("AES block must be 16 bytes")

    state = list(block)
    state = _add_round_key(state, round_keys[NR])

    for round_index in range(NR - 1, 0, -1):
        state = _inv_shift_rows(state)
        state = _inv_sub_bytes(state)
        state = _add_round_key(state, round_keys[round_index])
        state = _inv_mix_columns(state)

    state = _inv_shift_rows(state)
    state = _inv_sub_bytes(state)
    state = _add_round_key(state, round_keys[0])
    return bytes(state)


def _pkcs7_pad(data: bytes, block_size: int = BLOCK_SIZE) -> bytes:
    padding_len = block_size - (len(data) % block_size)
    if padding_len == 0:
        padding_len = block_size
    return data + bytes([padding_len]) * padding_len


def _pkcs7_unpad(data: bytes, block_size: int = BLOCK_SIZE) -> bytes:
    if not data or len(data) % block_size != 0:
        raise ValueError("Invalid padded data length")
    padding_len = data[-1]
    if padding_len < 1 or padding_len > block_size:
        raise ValueError("Invalid PKCS#7 padding length")
    if data[-padding_len:] != bytes([padding_len]) * padding_len:
        raise ValueError("Invalid PKCS#7 padding bytes")
    return data[:-padding_len]


def encrypt_ecb(data: bytes, key: bytes) -> bytes:
    round_keys = expand_key(key)
    padded = _pkcs7_pad(data)
    out = bytearray()
    for i in range(0, len(padded), BLOCK_SIZE):
        out.extend(encrypt_block(padded[i : i + BLOCK_SIZE], round_keys))
    return bytes(out)


def decrypt_ecb(data: bytes, key: bytes) -> bytes:
    if len(data) % BLOCK_SIZE != 0:
        raise ValueError("Ciphertext length must be a multiple of 16")
    round_keys = expand_key(key)
    out = bytearray()
    for i in range(0, len(data), BLOCK_SIZE):
        out.extend(decrypt_block(data[i : i + BLOCK_SIZE], round_keys))
    return _pkcs7_unpad(bytes(out))


def main() -> None:
    # FIPS-197 Appendix C.1 AES-128 known-answer test vector.
    key = bytes.fromhex("000102030405060708090a0b0c0d0e0f")
    plaintext = bytes.fromhex("00112233445566778899aabbccddeeff")
    expected_ciphertext = bytes.fromhex("69c4e0d86a7b0430d8cdb78070b4c55a")

    round_keys = expand_key(key)
    ciphertext = encrypt_block(plaintext, round_keys)
    recovered_plaintext = decrypt_block(ciphertext, round_keys)

    if ciphertext != expected_ciphertext:
        raise AssertionError(
            f"AES test vector mismatch: got={ciphertext.hex()} expected={expected_ciphertext.hex()}"
        )
    if recovered_plaintext != plaintext:
        raise AssertionError("AES block decryption failed to recover original plaintext")

    sample_message = b"AES MVP demo: pure Python implementation with explicit rounds."
    encrypted_message = encrypt_ecb(sample_message, key)
    decrypted_message = decrypt_ecb(encrypted_message, key)

    if decrypted_message != sample_message:
        raise AssertionError("ECB round-trip check failed")

    print("AES-128 known-answer test: PASS")
    print(f"Block plaintext : {plaintext.hex()}")
    print(f"Block ciphertext: {ciphertext.hex()}")
    print(f"Sample message (len={len(sample_message)}): {sample_message.decode('utf-8')}")
    print(f"ECB ciphertext  : {encrypted_message.hex()}")
    print("ECB round-trip  : PASS")


